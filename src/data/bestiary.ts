import type { Attack } from "@/types";

/**
 * Bestiario básico con criaturas del SRD 5.1, ordenado por dificultad.
 * `loot` es una expresión de dados de monedas de oro que suelta al morir;
 * `xp` son los PX que reparte al grupo cuando se saquea su sala.
 */
export interface MonsterDef {
  name: string;
  hp: number;
  ac: number;
  speed: number; // pies
  cr: string; // valor de desafío
  xp: number; // PX que otorga
  attacks: Attack[];
  abilities: string[]; // hechizos, trucos y rasgos, como referencia para el máster
  loot: string;
  image: string; // ilustración del SRD (dnd5eapi.co)
  emoji: string; // respaldo si la imagen no carga
}

/**
 * Las ilustraciones de dnd5eapi.co no declaran licencia ni procedencia, así
 * que van apagadas por defecto: sin la variable se usan los emojis. Quien
 * opere la instancia decide activarlas bajo su criterio.
 */
export const MONSTER_IMAGES_ENABLED =
  process.env.NEXT_PUBLIC_MONSTER_IMAGES === "on";

const img = (slug: string) =>
  `https://www.dnd5eapi.co/api/images/monsters/${slug}.png`;

const atk = (
  id: string,
  name: string,
  bonus: string,
  damage: string,
  type: string,
  range?: number // alcance en pies; sin valor = cuerpo a cuerpo
): Attack => ({
  id,
  name,
  bonus,
  damage,
  type,
  ...(range !== undefined && { range }),
});

